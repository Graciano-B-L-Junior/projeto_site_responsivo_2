				<h1>De volta à cidade</h1>
				<p>Streets of Rage 4 se passa em Wood Oak City e, novamente, lida com o tema bem básico de corrupção policial e violência, da mesma forma que vimos em títulos anteriores. Nesta versão, há o retorno de Axel Stone, Blaze Fielding e Adam Hunter como personagens clássicos, além de a estreia de Cherry Hunter e Floyd Iraia, alguns dos presentes jogáveis dentro do game. Com o tempo, mais nomes se enquadram no elenco, mas isso é apenas uma das surpresas que a aventura proporciona.
