				<p>Parece simples, e até é, mas Streets of Rage 4 adiciona mais caldo na magia. O jogo tem seus “pormenores”. É preciso aprender padrões de ataque dos inimigos, para saber como revidar, ou como atacar de maneira diferente, com o objetivo de não tomar dano. Além disso, cada um dos personagens se comportam de uma forma distinta. Axel e Blaze, por exemplo, são mais lentos que Cherry, por serem mais velhos, mas são mais fortes. Tudo é questão de costume e de eleger seu favorito para ir praticando.</p>
				<h1>Multiplayer</h1>
				<p>Como todo bom game no estilo “beat’em up”, Streets of Rage 4 tem modo multiplayer. Localmente, é possível jogar com até quatro participantes, cada um em um controle, como nos fliperamas antigos. Claro que isso vai exigir que o jogador tenha quatro controles, mas é sempre possível pedir que amigos tragam o seu próprio. Já o modo online é limitado para até dois participantes, mas não deixa de ser igualmente divertido, já que você pode aproveitar com qualquer pessoa do mundo.</p>
