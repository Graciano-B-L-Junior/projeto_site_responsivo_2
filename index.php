				<h1>E os gráficos, enfim</h1>
				<p>Streets of Rage 4 é um dos jogos 2D mais bonitos da atualidade. Simplesmente não há defeitos. Os personagens são bonitos, bem desenhados à mão, com boa proporção de tela, movimentos fluidos. Isso sem falar nos cenários. Luzes de neon refletem em todos os lugares. Efeitos climáticos mudam por inteiro o tom de uma fase.</p>
