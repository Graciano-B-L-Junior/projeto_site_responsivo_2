				A história de Streets of Rage 4 se desenrola conforme avançamos nas fases, mas não há muito a se destacar. Não é nada exatamente memorável a não ser o envolvimento dos personagens com os criminosos e seu senso de justiça, que os obriga a caçar e derrubar os chefões do crime da cidade. Todo o clima de filme policial dos anos 80 está de volta, mas entenda: é este o charme.</p>

				<h1>Controles nostálgicos ou modernos</h1>
				<p>Streets of Rage 4 pode ser fruto de um retorno da série clássica, no qual seu último título foi lançado há mais de 20 anos, mas nem por isso ele usa isso como desculpa para não se modernizar. Os controles, por exemplo, foram repaginados. Há muito mais opções para o que se fazer dentro das fases, combinando botões para ativar ataques variados. Hoje, ele lembra muito mais um jogo de luta do que as versões clássicas – mas calma, não quer dizer que tenha mudado drasticamente.</p>

