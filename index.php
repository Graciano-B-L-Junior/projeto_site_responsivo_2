				<h1>Conclusão</h1>
				<p>Streets of Rage 4 é um produto muito bom em termos de nostalgia, mas que não se prende apenas a isso. O jogo entrega um pacote completo: diversão, gráficos, som, jogabilidade e multiplayer. Há muitas opções a se explorar, destrancáveis como extras e configurações opcionais diversas. Sem falar no charme de ser um “beat’em up” bem produzido em pleno 2020, mostrando que o gênero ainda tem muito a oferecer, se bem explorado. Desde já é um dos games mais imperdíveis deste ano.</p>
