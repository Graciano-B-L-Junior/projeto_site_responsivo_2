				<h1>Como funciona o jogo?</h1>
				<p>Streets of Rage 4 segue de perto a fórmula que já vimos na série toda, caso você conheça. Se está chegando agora, não tem problema, explicamos: o game se divida em fases 2D. O personagem precisa andar sempre para a direita para avançar, lutando contra os inimigos que aparecerem pela frente em tempo real. Ao final, um chefão o aguarda, com muito mais vida e com um desafio lá no alto.</p>
