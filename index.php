				<p>Vale usar o review para avisar que Streets of Rage 4 não teve qualquer envolvimento direto da <a href="">SEGA</a>, sua antiga casa, apesar de fazer parte da série de maneira oficial graças à autorização concedida pela dona do <a href="">Sonic</a>. O game é fruto da união de três empresas diferentes: Dotemu, Lizardcube e Guard Crush Games. É importante citar isso por um simples motivo: bagagem prévia.</p>
